#pide dos numeros y has las 4 operaciones matematicas
num1 = int(input("Dime el primer numero: "));
num2 = int(input("Dime el segundo numero: "));

print ("La suma de " + str(num1) + " y " + str(num2) + " es: " + str(num1 + num2));
print ("La resta de " + str(num1) + " y " + str(num2) + " es: " + str(num1 - num2));
print ("La multiplicacion de " + str(num1) + " y " + str(num2) + " es: " + str(num1 * num2));
print ("La divicion de " + str(num1) + " y " + str(num2) + " es: " + str(num1 / num2));
